//HCSR04 (single) Ultrasonic Sensor for obstacle avoidance.  This code uses TIM1 (Timer 1) but may need to be changed to other timers depending on port availability

#include "main.h"

I2C_HandleTypeDef hi2c1;
TIM_HandleTypeDef htim1;

void SystemClock_Config(void);
static void MX_GPIO_Init(void);
static void MX_I2C1_Init(void);
static void MX_TIM1_Init(void);

void delay (uint16_t time)
{
	__HAL_TIM_SET_COUNTER(&htim1, 0);
	while (__HAL_TIM_GET_COUNTER (&htim1) < time);
}

uint32_t IC_Val1 = 0;
uint32_t IC_Val2 = 0;
uint32_t Difference = 0;
uint8_t Is_First_Captured = 0;  // is the first value captured ?
uint8_t Distance  = 0;

#define TRIG_PIN GPIO_PIN_8
#define TRIG_PORT GPIOA


void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
	if (htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1)  // if interrupt source is channel1
	{
		if (Is_First_Captured==0) // if first value not captured
		{
			IC_Val1 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1); // read first value
			Is_First_Captured = 1;  // set the first captured as true
			// Now change polarity to falling edge
			__HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_1, TIM_INPUTCHANNELPOLARITY_FALLING);
		}

		else if (Is_First_Captured==1)   // if first is captured already
		{
			IC_Val2 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);  // read second value
			__HAL_TIM_SET_COUNTER(htim, 0);  // reset counter

			if (IC_Val2 > IC_Val1)
			{
				Difference = IC_Val2-IC_Val1;
			}

			else if (IC_Val1 > IC_Val2)
			{
				Difference = (0xffff - IC_Val1) + IC_Val2;
			}

			Distance = Difference * .034/2;
			Is_First_Captured = 0; // set it back to false

			// set polarity to rising edge
			__HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_1, TIM_INPUTCHANNELPOLARITY_RISING);
			__HAL_TIM_DISABLE_IT(&htim1, TIM_IT_CC1);
		}
	}
}

void HCSR04_Read (void)
{
	HAL_GPIO_WritePin(TRIG_PORT, TRIG_PIN, GPIO_PIN_SET);  // pull TRIGGER pin HIGH
	delay(10);  // wait for 10 us
	HAL_GPIO_WritePin(TRIG_PORT, TRIG_PIN, GPIO_PIN_RESET);  // pull TRIGGER pin low

	__HAL_TIM_ENABLE_IT(&htim1, TIM_IT_CC1);
}

int main(void)
{
 
  HAL_Init();
  SystemClock_Config();
	
  MX_GPIO_Init();
  MX_I2C1_Init();
  MX_TIM1_Init();
  HAL_TIM_IC_Start_IT(&htim1, TIM_CHANNEL_1);
	
  while (1)
  {
   	  HCSR04_Read();
		
	  if (distance < 10)  //approaching obstacle, units of distance are CENTIMETERS
	  {
          	  left_target_rpm = 50;
		  right_target_rpm = 50;
	  } 
	  
	  else if (distance < 5) 
	  {
		  left_target_rpm = 50;
		  right_target_rpm = 50;
	  }
	  
	  else
	  {
		  left_target_rpm = 100;
		  right_target_rpm = 100;
	  } 
		
	  HAL_Delay(100);
  }  
}










